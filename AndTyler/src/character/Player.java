package character;

public class Player {

	String name;
	int gender;
	int age;
	int strength;
	int intelligence;
	int agility;
	int money;
	
	public Player(int a, int s, int i, int agi, int m){
		this.age=a;
		this.strength=s;
		this.intelligence=i;
		this.agility=agi;
		this.money=m;
	}
}
